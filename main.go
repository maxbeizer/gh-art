func main() {
	rootCmd := &cobra.Command{
		Use:   "gh-art",
		Short: "Terminal art screensaver for GitHub CLI",
		RunE: func(cmd *cobra.Command, args []string) error {
			interval, _ := cmd.Flags().GetDuration("interval")
			return runScreensaver(interval)
		},
	}

	rootCmd.Flags().Duration("interval", 8*time.Second, "rotation interval (e.g., 10s, 1m)")

	listCmd := &cobra.Command{
		Use:   "list",
		Short: "List available artworks",
		Run: func(cmd *cobra.Command, args []string) {
			for _, a := range artworks {
				fmt.Printf("%s\n", a.Name)
			}
		},
	}

	showCmd := &cobra.Command{
		Use:   "show <name>",
		Short: "Show a specific artwork",
		Args:  cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) error {
			name := args[0]
			for _, a := range artworks {
				if a.Name == name {
					drawArtwork(a)
					return nil
				}
			}
			return fmt.Errorf("unknown artwork: %s", name)
		},
	}

	rootCmd.AddCommand(listCmd, showCmd)

	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

func runScreensaver(interval time.Duration) error {
	oldState, err := term.MakeRaw(int(os.Stdin.Fd()))
	if err != nil {
		return err
	}
	defer term.Restore(int(os.Stdin.Fd()), oldState)

	// Hide cursor
	fmt.Print("\033[?25l")
	defer fmt.Print("\033[?25h")

	clearScreen()
	idx := 0
	drawArtwork(artworks[idx])

	inputCh := make(chan string, 1)
	go readInput(inputCh)

	sigCh := make(chan os.Signal, 1)
	signal.Notify(sigCh, os.Interrupt, syscall.SIGTERM)

	ticker := time.NewTicker(interval)
	defer ticker.Stop()

	for {
		select {
		case <-ticker.C:
			idx = (idx + 1) % len(artworks)
			clearScreen()
			drawArtwork(artworks[idx])
		case key := <-inputCh:
			switch key {
			case "q", "ctrl-c":
				clearScreen()
				return nil
			case "n", "tab":
				idx = (idx + 1) % len(artworks)
				clearScreen()
				drawArtwork(artworks[idx])
			case "p", "shift-tab":
				idx = (idx - 1 + len(artworks)) % len(artworks)
				clearScreen()
				drawArtwork(artworks[idx])
			}
		case <-sigCh:
			clearScreen()
			return nil
		}
	}
}

func readInput(ch chan<- string) {
	reader := bufio.NewReader(os.Stdin)
	for {
		b, err := reader.ReadByte()
		if err != nil {
			return
		}
		switch b {
		case 3: // Ctrl+C
			ch <- "ctrl-c"
		case 9: // Tab
			ch <- "tab"
		case 27: // ESC - start of escape sequence
			// Peek for [ Z (shift-tab)
			next, err := reader.ReadByte()
			if err != nil {
				continue
			}
			if next == '[' {
				code, err := reader.ReadByte()
				if err != nil {
					continue
				}
				if code == 'Z' {
					ch <- "shift-tab"
				}
			}
		default:
			ch <- string(b)
		}
	}
}

func drawArtwork(a Artwork) {
	width, height, err := term.GetSize(int(os.Stdout.Fd()))
	if err != nil || width == 0 || height == 0 {
		width = 80
		height = 24
	}

	lines := strings.Split(strings.Trim(a.Art, "\n"), "\n")
	info := []string{
		fmt.Sprintf("%s — %s", a.Artist, a.Title),
		a.Year,
	}

	// 2 lines padding top/bottom minimum, plus info lines + 1 blank separator
	padding := 2
	contentLines := len(lines) + len(info) + 1
	topPad := (height - contentLines) / 2
	if topPad < padding {
		topPad = padding
	}

	for i := 0; i < topPad; i++ {
		fmt.Print("\r\n")
	}

	for _, line := range lines {
		printCentered(line, width)
	}

	fmt.Print("\r\n")
	for _, line := range info {
		printCentered(line, width)
	}
}

func printCentered(line string, width int) {
	line = strings.TrimRight(line, "\n")
	if len(line) >= width {
		fmt.Printf("%s\r\n", line)
		return
	}
	pad := (width - len(line)) / 2
	fmt.Printf("%s%s\r\n", strings.Repeat(" ", pad), line)
}

func clearScreen() {
	fmt.Print("\033[2J\033[H")
}
